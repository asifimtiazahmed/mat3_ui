
/// We consider it is small screen if the width of the device
/// is less than 416.
const double smallScreen = 416.0;

/// We consider it is medium screen if the width of the device
/// is larger than than 416 but less than or equal to 768.
const double mediumScreen = 768.0;

/// We consider it is large screen if the width of the device
/// is larger than than 768 but less than or equal to 1440.
/// We consider it is very large screen if the width of the device
/// is larger than than 1440.
const double largeScreen = 1440.0;
